    private void cancelar() {
        atributos2Form();
        this.setVisible(false);
    }

    
    /**
     * Sale del formulario guardando los cambios.
     */    
    private void aceptar() {
        boolean ok_tag_contenedor = checkTagContenedor();
        if (ok_tag_contenedor) {
            form2Atributos();
            this.setVisible(false);
        }
    }
