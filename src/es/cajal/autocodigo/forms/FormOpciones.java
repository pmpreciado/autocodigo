    /**
     * Comprueba que, si el usuario ha especificado el tag del objeto contenedor, este conste de dos elementos: clase y nombre del objeto.
     * En caso de que no, muestra un mensaje de aviso y retorna 'false'.
     * 
     * @return                                  'true' si la comprobación ha sido correcta
     *                                          'false' si ha sido errónea
     */
    private boolean checkTagContenedor() {
        if (Comun.vacio(tag_contenedor)) {
            return true;
        }
        
        List <String> l_items_contenedor = Comun.split(tag_contenedor, " ");
        if (l_items_contenedor.size() != 2) {
            String mensaje = "El campo \"Clase y nombre objeto contenedor\" debe constar de dos elementos";
            es.cajal.autocodigo.Dialogos.showMessageDialog(cnt, this, mensaje, "Error", JOptionPane.ERROR_MESSAGE);
            
            return false;
        }
        
        return true;
    }
    

    /**
     * Establece el valor de los atributos indirectos a partir de los atributos introducidos en el formulario.
     * Si detecta algún error, muestra un mensaje al usuario.
     */
    private void inferirAtributosIndirectos() {
        
        // Objeto contenedor
        List <String> l_items_contenedor = Comun.split(tag_contenedor, " ");
        if (l_items_contenedor.size() != 2) {
            this.tag_contenedor_clase = null;
            this.tag_contenedor_nombre = null;
            
        } else {
            this.tag_contenedor_clase = l_items_contenedor.get(0);
            this.tag_contenedor_nombre = l_items_contenedor.get(1);
        }
        
        // Objeto secundario
        if (Comun.vacio(tag_objeto_secundario_bd)) {
            this.tag_objeto_secundario_bd_clase = null;
            this.tag_objeto_secundario_bd_nombre = null;
        } else {
            List <String> l_items_objeto_secundario = Comun.split(tag_objeto_secundario_bd, " ");
            if (l_items_objeto_secundario.size() != 2) {
                String mensaje = "Si se utiliza el campo \"Clase y nombre objeto secundario\", debe constar de dos elementos";
                es.cajal.autocodigo.Dialogos.showMessageDialog(cnt, this, mensaje, "Error", JOptionPane.ERROR_MESSAGE);

                this.tag_objeto_secundario_bd_clase = null;
                this.tag_objeto_secundario_bd_nombre = null;

            } else {
                this.tag_objeto_secundario_bd_clase = l_items_objeto_secundario.get(0);
                this.tag_objeto_secundario_bd_nombre = l_items_objeto_secundario.get(1);
            }
        }
    }
    
    
    
